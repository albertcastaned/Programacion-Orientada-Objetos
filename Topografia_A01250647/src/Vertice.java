public class Vertice {
    private int x;
    private int y;

    public Vertice(){
        x=0;
        y=0;
    }
    public Vertice(int ex,int ye){
        x=ex;
        y=ye;
    }

    public int getX() {
        return x;
    }
    public int getY(){
        return y;
    }
}
